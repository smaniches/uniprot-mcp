"""Live end-to-end provenance round-trip demonstration.

Runs the full chain against real UniProt and writes the observed
output to a transcript file. Captures: real Provenance values, the
verify tool's verdict for the matching case, and the verdicts for
hash-drift and release-drift scenarios. The transcript is the
evidence a reviewer cites when asked "does this actually work?"

Usage::

    python tests/benchmark/demo_roundtrip.py [output.md]

If no output path is given, writes to stdout. UTF-8 encoded — the
Markdown output uses ``✓`` / ``✗`` characters that some Windows
consoles cannot render directly.

SPDX-License-Identifier: Apache-2.0
"""

from __future__ import annotations

import asyncio
import json
import pathlib
import sys
from datetime import UTC, datetime
from io import StringIO

from uniprot_mcp.client import UniProtClient
from uniprot_mcp.server import uniprot_provenance_verify


async def _run(out: StringIO) -> int:
    out.write(
        f"# Provenance round-trip transcript — captured {datetime.now(tz=UTC).strftime('%Y-%m-%dT%H:%M:%SZ')}\n\n"
    )
    out.write(
        "Generated by `tests/benchmark/demo_roundtrip.py`. Live UniProt REST output — "
        "values observed at capture time may differ on subsequent runs as UniProt "
        "advances releases. The structural claims (Provenance shape, verify-tool "
        "verdicts) are stable.\n\n"
    )

    out.write("## Step 1 — real query to UniProt for P04637\n\n")
    client = UniProtClient()
    try:
        data = await client.get_entry("P04637")
        prov = client.last_provenance
    finally:
        await client.close()

    if prov is None:
        out.write("**ERROR:** client.last_provenance is None after a successful request.\n")
        return 2

    out.write("```\n")
    out.write(f"primaryAccession: {data['primaryAccession']}\n")
    out.write(f"gene:             {data['genes'][0]['geneName']['value']}\n")
    out.write(f"organism:         {data['organism']['scientificName']}\n")
    out.write(f"length:           {data['sequence']['length']}\n")
    out.write("```\n\n")

    out.write("## Step 2 — captured Provenance record (live values)\n\n")
    out.write("```json\n")
    out.write(json.dumps(prov, indent=2, ensure_ascii=False))
    out.write("\n```\n\n")

    out.write("## Step 3 — verify with captured fields (expected: `verified`)\n\n")
    verified = await uniprot_provenance_verify(
        url=prov["url"],
        release=prov["release"] or "",
        response_sha256=prov["response_sha256"],
        response_format="markdown",
    )
    verified_json = await uniprot_provenance_verify(
        url=prov["url"],
        release=prov["release"] or "",
        response_sha256=prov["response_sha256"],
        response_format="json",
    )
    out.write(verified)
    out.write("\n\n")
    out.write("```json\n")
    out.write(verified_json)
    out.write("\n```\n\n")
    verified_payload = json.loads(verified_json)
    if verified_payload["status"] != "verified":
        out.write(f"**FAIL:** expected `verified`, got `{verified_payload['status']}`\n")
        return 1

    out.write("## Step 4 — hash-drift demonstration (wrong recorded hash)\n\n")
    out.write(
        "Pass `response_sha256 = '0' * 64` to force a mismatch. Release tag still matches, so the verifier must report `hash_drift`.\n\n"
    )
    drift = await uniprot_provenance_verify(
        url=prov["url"],
        release=prov["release"] or "",
        response_sha256="0" * 64,
        response_format="markdown",
    )
    drift_json = await uniprot_provenance_verify(
        url=prov["url"],
        release=prov["release"] or "",
        response_sha256="0" * 64,
        response_format="json",
    )
    out.write(drift)
    out.write("\n\n")
    out.write("```json\n")
    out.write(drift_json)
    out.write("\n```\n\n")
    drift_payload = json.loads(drift_json)
    if drift_payload["status"] != "hash_drift":
        out.write(f"**FAIL:** expected `hash_drift`, got `{drift_payload['status']}`\n")
        return 1

    out.write("## Step 5 — release-drift demonstration (impossibly old release)\n\n")
    out.write(
        "Pass `release = '1999_01'` to force a mismatch. Body hash still matches, so the verifier must report `release_drift`.\n\n"
    )
    rdrift = await uniprot_provenance_verify(
        url=prov["url"],
        release="1999_01",
        response_sha256=prov["response_sha256"],
        response_format="markdown",
    )
    rdrift_json = await uniprot_provenance_verify(
        url=prov["url"],
        release="1999_01",
        response_sha256=prov["response_sha256"],
        response_format="json",
    )
    out.write(rdrift)
    out.write("\n\n")
    out.write("```json\n")
    out.write(rdrift_json)
    out.write("\n```\n\n")
    rdrift_payload = json.loads(rdrift_json)
    if rdrift_payload["status"] != "release_drift":
        out.write(f"**FAIL:** expected `release_drift`, got `{rdrift_payload['status']}`\n")
        return 1

    out.write("## Step 6 — url-unreachable demonstration (synthetic accession)\n\n")
    out.write(
        "Pass an accession-shaped URL that UniProt does not recognise. The verifier must classify the failure cleanly rather than raising.\n\n"
    )
    unreach = await uniprot_provenance_verify(
        url="https://rest.uniprot.org/uniprotkb/Q99999_NOT_A_REAL_ACC_42",
        response_format="markdown",
    )
    unreach_json = await uniprot_provenance_verify(
        url="https://rest.uniprot.org/uniprotkb/Q99999_NOT_A_REAL_ACC_42",
        response_format="json",
    )
    out.write(unreach)
    out.write("\n\n")
    out.write("```json\n")
    out.write(unreach_json)
    out.write("\n```\n\n")
    unreach_payload = json.loads(unreach_json)
    if unreach_payload["status"] != "url_unreachable":
        out.write(f"**FAIL:** expected `url_unreachable`, got `{unreach_payload['status']}`\n")
        return 1

    out.write("---\n\n")
    out.write("**Summary.** Every step produced the predicted verdict. The architectural\n")
    out.write("chain — *upstream → Provenance → markdown / JSON surface → verify tool re-fetches\n")
    out.write("→ matches* — works end-to-end against live UniProt at capture time.\n")
    return 0


def main(argv: list[str]) -> int:
    out_path: pathlib.Path | None = None
    if len(argv) > 1:
        out_path = pathlib.Path(argv[1])
        out_path.parent.mkdir(parents=True, exist_ok=True)
    buf = StringIO()
    rc = asyncio.run(_run(buf))
    text = buf.getvalue()
    if out_path is None:
        sys.stdout.reconfigure(encoding="utf-8", errors="replace")
        print(text)
    else:
        out_path.write_text(text, encoding="utf-8")
        print(f"wrote transcript to {out_path}")
    return rc


if __name__ == "__main__":
    sys.exit(main(sys.argv))
